#include "3-calc.h"
/**
*op_add -add to number
*@a:first number
*@b:second number
*Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - substract
*@a: number 1
*@b: number 2
*Return: a - b
*/
int op_mul(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiply a and b
*@a: number 1
*@b: number 2
*Return: a * b
*/
int op_mul(int a, int b)
{
	return (a + b);
}
/**
*op_div - divide a by b
*@a: first number
*@b: the factor
*Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - The remainder of a divided by b
*@a: number one
*@b: number two
*Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
