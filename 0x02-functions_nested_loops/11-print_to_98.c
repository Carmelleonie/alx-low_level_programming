#include <stdio.h>
#include "main.h"

/**
*print_to_98 - Entry point
*
*@n: print to 98
*This program prints prints the 9 times table, starting with 0
*Return: 0 Always (success)
*/
void print_to_98(int n)
{
	if (n < 98)
	{
		while (n <= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n++;
		}
	}
	else if (n > 98)
	{
		while (n >= 98)
		{
			printf("%d", n);
			if (n != 98)
			{
				printf(", ");
			}
			n--;
		}
	}
	else
	{
		printf("98");
	}
	printf("\n");
}
